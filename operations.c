
#include <stdio.h>

void main() {
    int first,second,add,sub,mul,div,mod;
    printf("Enter two numbers:\n");
    scanf("%d%d",&first,&second);
    add=first + second;
    sub=first - second;
    mul=first * second;
    div=first / second;
    mod=first % second;
    printf("Addition = %d\nSubtraction = %d\nMultiplication = %d\nDivision = %d\nModulo = %d\n",add,sub,mul,div,mod);
    
    
    
    
}